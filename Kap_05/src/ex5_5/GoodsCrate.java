package ex5_5;

public class GoodsCrate {
    public double height;
    public double width;
    public double depth;
    public double weight;

    /**
     * constructor of GoodsCrate
     * @param height the height of the crate
     * @param width the width of the crate
     * @param depth the depth of the crate
     * @param weight the weight of the crate
     */
    public GoodsCrate(double height, double width, double depth, double weight) {
        this.height = height;
        this.width = width;
        this.depth = depth;
        this.weight = weight;
    }
}
